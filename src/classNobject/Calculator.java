package classNobject;

public class Calculator {
	
	public int addition(int num1, int num2) {
		int total = num1 + num2;
		
		return total;
	}
	public int subtraction (int num2, int num3) {
		int total1 = num2 - num3;
		
		return total1;
	}
	public int multiplication (int num4, int num5) {
		int total2 = num4 * num5;
		
		return total2;
	}
	public int division (int num6, int num7) {
		int total3 = num6 / num7;
		
		return total3;
	}
	

}
